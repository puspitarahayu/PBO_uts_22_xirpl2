    private void selectData() {
        String kolom[] = {"Nama","Alamat","NoStruk","NoPolisi","TanggalPinjam","TanggalKembali"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM t_daftar";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while(rs.next()) {
                String Nama = rs.getString(1);
                String Alamat = rs.getString(2);
                String NoStruk = rs.getString(3);
                String NoPolisi = rs.getString(4);
                String TanggalPinjam = rs.getString(5);
                String TanggalKembali = rs.getString(6);
                String Hari = rs.getString(7);
                String Harga = rs.getString(8);
                String tabel[] = {Nama,Alamat,NoStruk,NoPolisi,TanggalPinjam,TanggalKembali,Hari,Harga};
                dtm.addRow(tabel);
            }
        } catch (SQLException ex) {
            Logger.getLogger(form_daftar.class.getName()).log(Level.SEVERE, null, ex);
        } tabel.setModel(dtm);
    }

