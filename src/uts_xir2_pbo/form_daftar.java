        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tglPinjam = dateFormat.format(Pinjam.getDate());
        String tglKembali = dateFormat.format(Kembali.getDate());
